/*

주제 : 자바에서 클래스안의 멤버변수(static을 붙이거나, 안붙인 모든 변수)들 종류 알아보자.
       ※ 모든 JVM 메모리 그림을 "세로 방향"으로 다시 그린 버전

🔸 1. 멤버 변수란?
클래스 안에 선언된 변수들을 멤버 변수 또는 필드(field)라고 합니다.
이 멤버 변수는 크게 2가지로 나눌 수 있습니다:

| 구분                     | 설명
| ------------------------| -------------------
|   객체  변수             | static이 없는 변수
| 클래스 변수 (static 변수) | static이 붙은 변수


class   A {

	//멤버변수 종류
	//1. 객체 변수 
	       int  num;
	
	//2.클래스 변수 
	static int  num;

}



💡 전제 설명: JVM 메모리 3대 핵심 구조 (세로 배치)

	┌─────────────────────────────┐
	│      Method Area            │ ← 클래스 정보, static 변수, 메서드 코드 저장
	│             class A         │    (클래스 로딩 시 static 변수 등 저장)
	├─────────────────────────────┤
	│         Stack               │ ← 메서드 호출 시 프레임 저장, 지역변수 존재
	│                             │    (메서드 실행 시마다 쌓이고 사라짐)
	├─────────────────────────────┤
	│         Heap                │ ← new 객체 생성 시  객체변수 저장
	│new A();  new A();  new A();    (new 연산자로 객체 생성할 때 저장)
	└─────────────────────────────┘


🧩 2. 인스턴스(객체) 변수
	✅ 정의:
	 - static 키워드 없이 선언된 멤버 변수
	 - 객체가 생성될 때마다 따로 존재

	✅ 메모리:
	 - 객체가 생성될 때마다 JVM의 Heap 메모리에 따로 저장됩니다.

	✅ 특징:
	 - 각 객체마다 고유한 값을 가질 수 있음
	 - 객체가 사라지면 같이 사라짐

🧩 3. 클래스 변수 (class variable, static 변수)
	✅ 정의:
	 - static 키워드가 붙은 멤버 변수
	 - 클래스가 JVM메모리의  Method Area영역에 올라갈 때 한 번만 생성됨
	 - 모든 객체가 공유

	✅ 메모리:
	 - JVM의 Method Area (정적 메모리 공간) 영역에 저장

	✅ 특징:
	 - 객체 없이도 사용 가능: 클래스이름.변수명으로 접근
	 - 모든 객체가 같은 값을 공유
	 - 메모리를 절약할 수 있음
*/
public class Ex1 {
	
//멤버변수 종류 
	//종류1.  객체 변수
	//		 - static이 없으므로 객체메모리에 포함된 변수 
	//		 - new Ex1(); 객체 메모리 생성하면 객체 메모리에 포함된 변수 
	//		 - JVM 메모리 영역 중에서 heap영역에 올라가는 객체 메모리 내부의 개별적 공간을 가지는 변수 
			 int  x;
	
	//종류2.  클래스 변수 
	//		 - static 작성해서 만든 변수 
	//		 - public class Ex1 가 처음 JVM 메모리의 Method area 영역에 올라갈때  포함된 변수
	//       - 객체 메모리를 생성하지 않고도   클래스명.클래스변수명 을 작성해서 접근 가능한 변수 
	  static  int y;
	  
//생성자 
	public Ex1(int z) {    //<----  new Ex1(10);
		
		this.x  += z; //JVM 메모리의 heap영역에 올라가는 객체변수 x 에  z매개변수로 전달 받은 값 누적
		
	   	 Ex1.y  += z; //JVM 메모리의 Method area영역에 올라가는 클래스변수 x 에  z매개변수로 전달 받은 값 누적 		
	}
	  
	
	public static void main(String[] args) {
		
		Ex1 ex1 = new Ex1(10);
		
		System.out.println("객체변수 x = " + ex1.x);   //객체변수 x = 10
		System.out.println("클래스변수 y = " + Ex1.y); //클래스변수 y = 10
		/*
		 * 【new Ex1(10) 1번째 실행 후】 (세로 그림)
		 * ┌─────────────────────────────┐
		 * │      Method Area            │
		 * │ ┌─────────────────────────┐ │
		 * │ │ class Ex1              │ │
		 * │ │ static int y = 10       │ │ ← 0 + 10 = 10 (누적)
		 * │ └─────────────────────────┘ │
		 * ├─────────────────────────────┤
		 * │         Stack               │
		 * │ ┌─────────────────────────┐ │
		 * │ │ main() 프레임             │ │
		 * │ │   ex1 [ 0x100 ] ──┐     │ │ ← 참조변수는 객체 "주소"만 저장
		 * │ └────────────────────┼────┘ │
		 * ├──────────────────────┼──────┤
		 * │         Heap         │      │
		 * │ ┌────────────────────▼────┐ │
		 * │ │ Ex1 객체 @0x100         │ │ ← test가 참조하는 객체
		 * │ │   int x = 10            │ │
		 * │ └─────────────────────────┘ │
		 * └─────────────────────────────┘
		 */	
		
		Ex1  ex2 = new Ex1(10);
		
		System.out.println("객체변수 x = " + ex2.x);   //객체변수 x = 10
		System.out.println("클래스변수 y = " + Ex1.y); //클래스변수 y = 20
		/*
		 * 【new Ex1(10) 2번째 실행 후】 (세로 그림)
		 *  - Heap의 객체는 "세로로" 하나씩 쌓인다 (객체 2개, x 객체 변수도 2개)
		 *  - Method Area의 y는 여전히 "1개"이고 값만 누적된다
		 *
		 * ┌─────────────────────────────┐
		 * │      Method Area            │
		 * │ ┌─────────────────────────┐ │
		 * │ │ class Ex1               │ │
		 * │ │ static int y = 20       │ │ ← 10 + 10 = 20 (하나에 누적)
		 * │ └─────────────────────────┘ │
		 * ├─────────────────────────────┤
		 * │         Stack               │
		 * │ ┌─────────────────────────┐ │
		 * │ │ main() 프레임             │ │
		 * │ │   ex1 [ 0x100 ]        │ │
		 * │ │   ex2   [ 0x200 ]        │ │
		 * │ └─────────────────────────┘ │
		 * ├─────────────────────────────┤
		 * │         Heap                │
		 * │ ┌─────────────────────────┐ │
		 * │ │ Ex1 객체 @0x100         │ │ ← test가 참조
		 * │ │   int x = 10            │ │
		 * │ └─────────────────────────┘ │
		 * │ ┌─────────────────────────┐ │
		 * │ │ Ex1 객체 @0x200         │ │ ← t1이 참조
		 * │ │   int x = 10            │ │    (x는 객체마다 개별로 존재!)
		 * │ └─────────────────────────┘ │
		 * └─────────────────────────────┘
		 */		
		  
		  Ex1  ex3 = new Ex1(10);
		
			System.out.println("객체변수 x = " + ex3.x);   //객체변수 x = 10
			System.out.println("클래스변수 y = " + Ex1.y); //클래스변수 y = 30
											//클래스명.클래스변수명
											//참조변수명.클래스변수명 
			
		/*
		 * 【new Ex1(10) 3번째 실행 후】 (세로 그림)
		 *  - Heap: 객체 3개가 세로로 쌓임 -> x도 3개 (각자 10)
		 *  - Method Area: y는 끝까지 1개 -> 값만 30으로 누적
		 *
		 * ┌─────────────────────────────┐
		 * │      Method Area            │
		 * │ ┌─────────────────────────┐ │
		 * │ │ class Ex1              │ │
		 * │ │ static int y = 30       │ │ ← 20 + 10 = 30 (계속 하나에 누적)
		 * │ └─────────────────────────┘ │
		 * ├─────────────────────────────┤
		 * │         Stack               │
		 * │ ┌─────────────────────────┐ │
		 * │ │ main() 프레임             │ │
		 * │ │   ex1   [ 0x100 ]        │ │
		 * │ │   ex2   [ 0x200 ]        │ │
		 * │ │   ex3   [ 0x300 ]        │ │
		 * │ └─────────────────────────┘ │
		 * ├─────────────────────────────┤
		 * │         Heap                │
		 * │ ┌─────────────────────────┐ │
		 * │ │ Ex1 객체 @0x100         │ │ ← ex1참조변수가 참조
		 * │ │   int x = 10            │ │
		 * │ └─────────────────────────┘ │
		 * │ ┌─────────────────────────┐ │
		 * │ │ Ex1 객체 @0x200         │ │ ←ex2참조변수가 참조
		 * │ │   int x = 10            │ │
		 * │ └─────────────────────────┘ │
		 * │ ┌─────────────────────────┐ │
		 * │ │ Ex1 객체 @0x300         │ │ ← ex3참조변수가 참조
		 * │ │   int x = 10            │ │
		 * │ └─────────────────────────┘ │
		 * └─────────────────────────────┘
		 */			
			
			/*
			결론
				출력결과를 보면 static으로 선언된 클래스변수 y는?
				클래스에 포함되어 JVM메모리에 올라가서 생성되므로
				위의 ex1, ex2참조변수의 객체가 클래스변수 y를 하나 공유받아 사용하므로
				클래스변수 y안의 값이 계속 += 10 누적된다고 보면 된다.

				출력결과를 보면 객체를 생성할때 객체 메모리 내부에 만들어지는 x변수는?
				객체를 생성할때마다 각각의 객체메모리에 각각개별로 JVM메모리의
				heap영역의 x객체변수 메모리가 생성됨
				그러므로 각각 x객체변수 3개는 각각 +=에의해 10이 저장됨

				★세로 그림으로 본 핵심★
				- Heap 칸    : new 할 때마다 객체 상자가 "세로로 1개씩 추가" (x도 같이 추가)
				- Method Area: 처음부터 끝까지 클래스변수 y 상자는 "딱 1개", 값만 10→20→30 누적
			*/			
			
			
		
	}

}







